
-- ====================================================================
-- 2. CREATE TABLES
-- ====================================================================
CREATE TABLE "DITTA" (
"PARTITA_IVA" VARCHAR2(11),
"NOME" VARCHAR2(50) NOT NULL,
"CELLULARE" VARCHAR2(13) NOT NULL,
"VIA" VARCHAR2(50) NOT NULL,
"CITTA" VARCHAR2(50) NOT NULL,
"CIVICO" VARCHAR2(50) NOT NULL,
PRIMARY KEY ("PARTITA_IVA")
);
CREATE TABLE "EQUIPAGGIAMENTO" (
"CODICE_A_BARRE" VARCHAR2(13),
"TIPO" VARCHAR2(20) NOT NULL,
"NOME" VARCHAR2(20) NOT NULL,
"TAGLIA" VARCHAR2(3) NOT NULL,
PRIMARY KEY ("CODICE_A_BARRE")
);CREATE TABLE "FATTURA" (
"CODICE_FATTURA" VARCHAR2(20),
"NOME_DITTA" VARCHAR2(50) NOT NULL,
"NOME" VARCHAR2(50) NOT NULL,
"COGNOME" VARCHAR2(50) NOT NULL,
CONSTRAINT "CHK_FATTURA_MAIUSCOLO" CHECK (CODICE_FATTURA = UPPER(CODICE_FATTURA)),
PRIMARY KEY ("CODICE_FATTURA")
);
CREATE TABLE "GESTORE" (
"CODICE_FISCALE" VARCHAR2(16),
"NOME" VARCHAR2(20) NOT NULL,
"COGNOME" VARCHAR2(20) NOT NULL,
"EMAIL" VARCHAR2(30) NOT NULL,
"CELLULARE" VARCHAR2(13) NOT NULL,
PRIMARY KEY ("CODICE_FISCALE")
);
CREATE TABLE "KART" (
"NUMERO_TELAIO" VARCHAR2(17),
"NUMERO_KART" VARCHAR2(10),
"MODELLO" VARCHAR2(50) NOT NULL,
"CILINDRATA" NUMBER(4, 0) NOT NULL,
CONSTRAINT "PK_KART" PRIMARY KEY ("NUMERO_KART", "NUMERO_TELAIO")
);
CREATE TABLE "PILOTA" (
"LICENZA" VARCHAR2(40),
"NOME" VARCHAR2(20) NOT NULL,
"COGNOME" VARCHAR2(20) NOT NULL,
"PESO" NUMBER(3, 0) NOT NULL,
"CATEGORIA" VARCHAR2(20) NOT NULL,
"DATA_NASCITA" DATE NOT NULL,
PRIMARY KEY ("LICENZA")
);
CREATE TABLE "PISTA" (
"NOME_PISTA" VARCHAR2(30),
"LUNGHEZZA_METRI" NUMBER(4, 0) NOT NULL,
PRIMARY KEY ("NOME_PISTA")
);
CREATE TABLE "TEAM" (
"NOME_TEAM" VARCHAR2(30),
"SEDE" VARCHAR2(30) NOT NULL,"EMAIL_AZIENDALE" VARCHAR2(50) NOT NULL,
CONSTRAINT "CK_TEAM_EMAIL_FORMATO" CHECK (EMAIL_AZIENDALE LIKE '%@%.%'),
PRIMARY KEY ("NOME_TEAM")
);
CREATE TABLE "UTENTE" (
"CELLULARE" VARCHAR2(13),
"NOME" VARCHAR2(20) NOT NULL,
"COGNOME" VARCHAR2(20) NOT NULL,
PRIMARY KEY ("CELLULARE")
);
CREATE TABLE "AFFITTA" (
"CELLULARE" VARCHAR2(13) NOT NULL,
"NUMERO_TELAIO" VARCHAR2(17) NOT NULL,
"NUMERO_KART" VARCHAR2(10) NOT NULL,
CONSTRAINT "PK_AFFITTA" PRIMARY KEY ("CELLULARE", "NUMERO_TELAIO", "NUMERO_KART")
);
CREATE TABLE "AMMINISTRATORE" (
"CODICE_FISCALE" VARCHAR2(16),
PRIMARY KEY ("CODICE_FISCALE")
);
CREATE TABLE "GARA" (
"DATA_GARA" DATE,
"ORA_GARA" VARCHAR2(5),
"NOME_CAMPIONATO" VARCHAR2(20),
"NOME_PISTA" VARCHAR2(30) NOT NULL,
CONSTRAINT "CK_ORA_FORMATO_CORRETTO" CHECK (ORA_GARA >= '00:00' AND ORA_GARA <=
'23:59'),
CONSTRAINT "PK_GARA" PRIMARY KEY ("NOME_PISTA", "DATA_GARA", "ORA_GARA")
);
CREATE TABLE "INGAGGIA" (
"LICENZA" VARCHAR2(40),
"NOME_TEAM" VARCHAR2(30),
"ANNO" NUMBER(4, 0),
CONSTRAINT "CK_INGAGGIA_ANNO" CHECK (ANNO >= 2000 AND ANNO <= 2100),
CONSTRAINT "PK_INGAGGIA" PRIMARY KEY ("LICENZA", "NOME_TEAM", "ANNO")
);
CREATE TABLE "MANUTENTORE" (
"CODICE_FISCALE" VARCHAR2(16),
PRIMARY KEY ("CODICE_FISCALE")
);CREATE TABLE "PACCHETTO_GARA" (
"NOME_PACCHETTO" VARCHAR2(30),
"DURATA" NUMBER(3, 0) NOT NULL,
"PREZZO" NUMBER(8, 2) NOT NULL,
"NUMERO_PARTECIPANTI" NUMBER(4, 0) NOT NULL,
"NOME_PISTA" VARCHAR2(30) NOT NULL,
PRIMARY KEY ("NOME_PACCHETTO")
);
CREATE TABLE "PRENOTAZIONE" (
"DATA_PRENOTAZIONE" DATE,
"ORA_PRENOTAZIONE" VARCHAR2(5),
"QUOTA_ISCRIZIONE" NUMBER(3, 0),
"NUMERO_PARTECIPANTI_EFFETTIVO" NUMBER(3, 0),
"NOME_PISTA" VARCHAR2(30) NOT NULL,
"DATA_GARA" DATE NOT NULL,
"ORA_GARA" VARCHAR2(5) NOT NULL,
"CELLULARE" VARCHAR2(13) NOT NULL,
"NOME_PACCHETTO" VARCHAR2(30) NOT NULL,
"CODICE_FISCALE" VARCHAR2(16) NOT NULL,
CONSTRAINT "CK_PRENOT_ORA_FORMATO" CHECK (ORA_PRENOTAZIONE >= '00:00' AND
ORA_PRENOTAZIONE <= '23:59'),
CONSTRAINT "CK_PRENOT_QUOTA_POSITIVA" CHECK (QUOTA_ISCRIZIONE >= 0),
CONSTRAINT "CK_PRENOT_PART_POSITIVI" CHECK (NUMERO_PARTECIPANTI_EFFETTIVO > 0),
CONSTRAINT "PK_PRENOTAZIONE" PRIMARY KEY ("DATA_PRENOTAZIONE", "ORA_PRENOTAZIONE")
);
CREATE TABLE "RICHIEDE" (
"CELLULARE" VARCHAR2(13),
"CODICE_A_BARRE" VARCHAR2(13),
"DATA_RICHIESTA" DATE,
"ORA_RICHIESTA" VARCHAR2(5),
CONSTRAINT "CK_RICHIEDE_ORA_FORMATO" CHECK (ORA_RICHIESTA >= '00:00' AND ORA_RICHIESTA
<= '23:59'),
CONSTRAINT "PK_RICHIEDE" PRIMARY KEY ("CELLULARE", "CODICE_A_BARRE", "DATA_RICHIESTA",
"ORA_RICHIESTA")
);
CREATE TABLE "TELEMETRIA" (
"NUMERO_GIRO" NUMBER(3, 0),
"TEMPO_GIRO" INTERVAL DAY (0) TO SECOND (3) NOT NULL,
"POSIZIONE" NUMBER(2, 0) NOT NULL,
"DATA_GARA" DATE,
"ORA_GARA" VARCHAR2(5),
"NUMERO_TELAIO" VARCHAR2(17),"NUMERO_KART" VARCHAR2(10),
"LICENZA" VARCHAR2(40),
"NOME_PISTA" VARCHAR2(30),
CONSTRAINT "CK_TELEMETRIA_GIRO_VALIDO" CHECK (NUMERO_GIRO > 0),
CONSTRAINT "CK_TELEMETRIA_POS_VALIDA" CHECK (POSIZIONE > 0),
CONSTRAINT "CK_TELEMETRIA_TIPO_PILOTA" CHECK (
("LICENZA" IS NOT NULL AND "NUMERO_TELAIO" IS NULL AND "NUMERO_KART" IS NULL)
OR
("LICENZA" IS NULL AND "NUMERO_TELAIO" IS NOT NULL AND "NUMERO_KART" IS NOT NULL)
),
CONSTRAINT "PK_TELEMETRIA" PRIMARY KEY ("NOME_PISTA", "DATA_GARA", "ORA_GARA",
"NUMERO_GIRO", "POSIZIONE")
);
CREATE TABLE "INTERVENTO_RIPARAZIONE" (
"CODICE_INTERVENTO" NUMBER(4, 0),
"DATA_INIZIO" DATE NOT NULL,
"DATA_FINE" DATE,
"ORA_INIZIO" VARCHAR2(5) NOT NULL,
"ORA_FINE" VARCHAR2(5),
"TIPO_GUASTO" VARCHAR2(500),
"TIPI_RICAMBI" VARCHAR2(500),
"COSTO" NUMBER(8, 2),
"CODICE_FATTURA" VARCHAR2(20) NOT NULL,
"NUMERO_KART" VARCHAR2(10) NOT NULL,
"NUMERO_TELAIO" VARCHAR2(17) NOT NULL,
"PARTITA_IVA" VARCHAR2(11) NOT NULL,
"CODICE_FISCALE" VARCHAR2(16),
PRIMARY KEY ("CODICE_INTERVENTO"),
CONSTRAINT "CK_INTERVENTO_ORA_INIZIO" CHECK (ORA_INIZIO >= '00:00' AND ORA_INIZIO <=
'23:59'),
CONSTRAINT "CK_INTERVENTO_ORA_FINE" CHECK (ORA_FINE >= '00:00' AND ORA_FINE <= '23:59')
);
CREATE TABLE "PAGAMENTO" (
"DATA_PAGAMENTO" DATE,
"ORA_PAGAMENTO" VARCHAR2(5),
"MODALITA" VARCHAR2(30),
"DATA_PRENOTAZIONE" DATE,
"ORA_PRENOTAZIONE" VARCHAR2(5),
"NOME_TEAM" VARCHAR2(30),
CONSTRAINT "CK_PAGAMENTO_ORA_FORMATO" CHECK (ORA_PAGAMENTO >= '00:00' AND
ORA_PAGAMENTO <= '23:59'),
CONSTRAINT "CK_PAGAMENTO_MODALITA" CHECK (MODALITA IN ('Contanti', 'Carta Credito',
'Bancomat', 'Bonifico')),
CONSTRAINT "PK_PAGAMENTO" PRIMARY KEY ("DATA_PAGAMENTO", "ORA_PAGAMENTO")
);-- ====================================================================

-- VISTA MANUTENTORE: Interventi di riparazione attivi con dettagli della ditta
CREATE OR REPLACE VIEW V_MANUTENZIONE_ATTIVA AS
SELECT I.CODICE_INTERVENTO, I.NUMERO_KART, I.NUMERO_TELAIO, I.TIPO_GUASTO, 
       D.NOME AS NOME_DITTA, D.CELLULARE AS CONTATTO_DITTA
FROM INTERVENTO_RIPARAZIONE I
JOIN DITTA D ON I.PARTITA_IVA = D.PARTITA_IVA
WHERE I.DATA_FINE IS NULL;

-- VISTA AMMINISTRATORE: Riepilogo prenotazioni collegate ai dati del cliente
CREATE OR REPLACE VIEW V_REPORT_PRENOTAZIONI AS
SELECT P.DATA_PRENOTAZIONE, P.ORA_PRENOTAZIONE, P.DATA_GARA, 
       U.NOME, U.COGNOME, U.CELLULARE, 
       P.NOME_PACCHETTO, P.NOME_PISTA
FROM PRENOTAZIONE P
JOIN UTENTE U ON P.CELLULARE = U.CELLULARE;

-- VISTA AMMINISTRATORE: Associazione piloti-team per la stagione in corso
CREATE OR REPLACE VIEW V_PILOTI_TEAM_ATTIVI AS
SELECT P.NOME, P.COGNOME, P.CATEGORIA, I.NOME_TEAM, I.ANNO
FROM PILOTA P
JOIN INGAGGIA I ON P.LICENZA = I.LICENZA
WHERE I.ANNO = EXTRACT(YEAR FROM SYSDATE);

-- VISTA AMMINISTRATORE: Report cronologico di tutti i giri della gara
CREATE OR REPLACE VIEW V_REPORT_GARA_COMPLETO AS
SELECT 
    T.DATA_GARA, 
    T.ORA_GARA, 
    T.NOME_PISTA, 
    T.NUMERO_GIRO, 
    T.POSIZIONE, 
    COALESCE(P.COGNOME || ' ' || P.NOME, T.LICENZA) AS PILOTA,
    T.TEMPO_GIRO, 
    T.NUMERO_KART
FROM TELEMETRIA T
LEFT JOIN PILOTA P ON T.LICENZA = P.LICENZA;

-- VISTA AMMINISTRATORE: Classifica di arrivo finale basata sull'ultimo giro della gara
CREATE OR REPLACE VIEW V_CLASSIFICA_FINALE_GARA AS
SELECT 
    T.DATA_GARA, 
    T.ORA_GARA, 
    T.NOME_PISTA, 
    T.POSIZIONE AS POSIZIONE_FINALE, 
    COALESCE(P.COGNOME || ' ' || P.NOME, T.LICENZA) AS PILOTA,
    T.NUMERO_KART,
    T.NUMERO_GIRO AS GIRI_TOTALI
FROM TELEMETRIA T
LEFT JOIN PILOTA P ON T.LICENZA = P.LICENZA
WHERE T.NUMERO_GIRO = (
    SELECT MAX(T2.NUMERO_GIRO) 
    FROM TELEMETRIA T2 
    WHERE T2.DATA_GARA = T.DATA_GARA 
      AND T2.ORA_GARA = T.ORA_GARA 
      AND T2.NOME_PISTA = T.NOME_PISTA
);


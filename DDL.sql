    TELEMETRIA T
LEFT JOIN PILOTA P ON
    T.LICENZA = P.LICENZA
WHERE
    T.NUMERO_GIRO = (
    SELECT
        MAX(T2.NUMERO_GIRO)
    FROM
        TELEMETRIA T2
    WHERE
        T2.DATA_GARA = T.DATA_GARA
        AND T2.ORA_GARA = T.ORA_GARA
        AND T2.NOME_PISTA = T.NOME_PISTA
);


CREATE OR REPLACE
FORCE EDITIONABLE VIEW "V_CLASSIFICA_FINALE_GARA" ("DATA_GARA",
"ORA_GARA",
"NOME_PISTA",
"POSIZIONE_FINALE",
"PILOTA",
"NUMERO_KART",
"GIRI_TOTALI") AS 
  SELECT 
    T.DATA_GARA, 
    T.ORA_GARA, 
    T.NOME_PISTA, 
    T.POSIZIONE AS POSIZIONE_FINALE, 
    COALESCE(P.COGNOME || ' ' || P.NOME, T.LICENZA) AS PILOTA,
    T.NUMERO_KART,

CREATE OR REPLACE
PROCEDURE KARTODROMO.SP_AGGIORNA_DETTAGLI_RIPARAZIONE (
    p_cod_intervento NUMBER,
p_tipo_guasto VARCHAR2,
p_tipi_ricambi VARCHAR2
) IS
    v_num_kart VARCHAR2(100);

v_partita_iva VARCHAR2(100);

v_check NUMBER;

BEGIN
    BEGIN
        SELECT
    NUMERO_KART,
    PARTITA_IVA
INTO
    v_num_kart,
    v_partita_iva
FROM
    INTERVENTO_RIPARAZIONE
WHERE
    CODICE_INTERVENTO = p_cod_intervento
    AND DATA_FINE IS NULL;

EXCEPTION
WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20095, 'Errore: Intervento inesistente o gia chiuso.');
END;

SELECT
    COUNT(*)
INTO
    v_check
FROM
    KART
WHERE
    NUMERO_KART = v_num_kart;

IF v_check = 0 THEN
        RAISE_APPLICATION_ERROR(-20061, 'Errore: Integrita violata, il Kart associato non esiste piu.');
END IF;

SELECT
    COUNT(*)
INTO
    v_check
FROM
    DITTA
WHERE
    PARTITA_IVA = v_partita_iva;

IF v_check = 0 THEN
        RAISE_APPLICATION_ERROR(-20062, 'Errore: Integrita violata, la Ditta associata non esiste piu.');
END IF;

UPDATE
    INTERVENTO_RIPARAZIONE
SET
    TIPO_GUASTO = p_tipo_guasto,
    TIPI_RICAMBI = p_tipi_ricambi
WHERE
    CODICE_INTERVENTO = p_cod_intervento;
END;


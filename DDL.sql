CREATE OR REPLACE
EDITIONABLE TRIGGER "T_ETA_MINIMA_INGAGGIO_TEAM" 
BEFORE
INSERT
    OR
UPDATE
    ON
    INGAGGIA FOR EACH ROW
DECLARE
        v_data_nascita DATE;

v_eta_nell_anno NUMBER;
BEGIN
    SELECT
    DATA_NASCITA
INTO
    v_data_nascita
FROM
    PILOTA
WHERE
    LICENZA = :NEW.LICENZA;

v_eta_nell_anno := :NEW.ANNO - EXTRACT(YEAR FROM v_data_nascita);

IF v_eta_nell_anno <= 14 THEN
        RAISE_APPLICATION_ERROR(-20080, 'Errore: Il pilota deve avere più di 14 anni.');
END IF;

EXCEPTION
WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20001, 'Pilota non trovato.');
END;

/
ALTER TRIGGER "T_ETA_MINIMA_INGAGGIO_TEAM" ENABLE;


-- 1. Verifica età 
CREATE OR REPLACE TRIGGER T_ETA_MINIMA_INGAGGIO_TEAM
BEFORE INSERT OR UPDATE ON INGAGGIA FOR EACH ROW
DECLARE v_data_nascita DATE; v_eta_nell_anno NUMBER;
BEGIN
    SELECT DATA_NASCITA INTO v_data_nascita FROM PILOTA WHERE LICENZA = :NEW.LICENZA;
    v_eta_nell_anno := :NEW.ANNO - EXTRACT(YEAR FROM v_data_nascita);
    IF v_eta_nell_anno <= 14 THEN
        RAISE_APPLICATION_ERROR(-20080, 'Errore: Il pilota deve avere più di 14 anni.');
    END IF;
EXCEPTION
    WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20001, 'Pilota non trovato.');
END;
/
-- 2. Data fine riparazione
CREATE OR REPLACE TRIGGER T_COERENZA_DATE_RIPARAZIONE
BEFORE INSERT OR UPDATE ON INTERVENTO_RIPARAZIONE FOR EACH ROW
BEGIN
    IF :NEW.DATA_FINE IS NOT NULL THEN
        IF :NEW.DATA_FINE < :NEW.DATA_INIZIO THEN
            RAISE_APPLICATION_ERROR(-20051, 'Data fine antecedente a inizio.');
        ELSIF :NEW.DATA_FINE = :NEW.DATA_INIZIO AND :NEW.ORA_FINE <= :NEW.ORA_INIZIO THEN
            RAISE_APPLICATION_ERROR(-20052, 'Ora fine non valida.');
        END IF;
    END IF;
END;
/
-- 3. Prenotazione futura
CREATE OR REPLACE TRIGGER T_PRENOTAZIONE_FUTURA
BEFORE INSERT ON PRENOTAZIONE FOR EACH ROW
BEGIN
    IF :NEW.DATA_GARA < TRUNC(SYSDATE) THEN
        RAISE_APPLICATION_ERROR(-20053, 'Operazione negata: gara passata.');
    END IF;
END;
/
-- 4. Minimo partecipanti 
CREATE OR REPLACE TRIGGER MINIMO_PARTECIPANTI
BEFORE INSERT OR UPDATE ON PRENOTAZIONE FOR EACH ROW
DECLARE MINIMO_RICHIESTO NUMBER;
BEGIN
    SELECT NUMERO_PARTECIPANTI INTO MINIMO_RICHIESTO FROM PACCHETTO_GARA WHERE NOME_PACCHETTO = :NEW.NOME_PACCHETTO;
    IF :NEW.NUMERO_PARTECIPANTI_EFFETTIVO < MINIMO_RICHIESTO THEN
        RAISE_APPLICATION_ERROR(-20002, 'Partecipanti sotto il minimo.');
    END IF;
EXCEPTION
    WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20004, 'Pacchetto gara non trovato.');
END;

/
-- 5. Date pagamento
CREATE OR REPLACE TRIGGER T_COERENZA_DATE_PAGAMENTO
BEFORE INSERT OR UPDATE ON PAGAMENTO FOR EACH ROW
BEGIN
    IF :NEW.DATA_PAGAMENTO < :NEW.DATA_PRENOTAZIONE THEN
        RAISE_APPLICATION_ERROR(-20041, 'Pagamento precedente alla prenotazione.');
    END IF;
END;

/
-- 6. Pagamento consolidato

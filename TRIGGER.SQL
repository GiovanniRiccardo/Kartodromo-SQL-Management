BEFORE UPDATE ON PAGAMENTO FOR EACH ROW
BEGIN
    RAISE_APPLICATION_ERROR(-20042, 'Vietato modificare un pagamento consolidato.');
END;

/
-- 7. Limite piloti
CREATE OR REPLACE TRIGGER T_LIMITE_PILOTI_TEAM
BEFORE INSERT ON INGAGGIA FOR EACH ROW
DECLARE v_conteggio NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_conteggio FROM INGAGGIA WHERE NOME_TEAM = :NEW.NOME_TEAM AND ANNO = :NEW.ANNO;
    IF v_conteggio >= 2 THEN
        RAISE_APPLICATION_ERROR(-20046, 'Un team non può ingaggiare più di 2 piloti l''anno.');
    END IF;
END;

/
-- 8. Kart libero 
CREATE OR REPLACE TRIGGER T_CHECK_KART_LIBERO
BEFORE INSERT ON INTERVENTO_RIPARAZIONE FOR EACH ROW
DECLARE v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count FROM INTERVENTO_RIPARAZIONE 
    WHERE NUMERO_KART = :NEW.NUMERO_KART AND NUMERO_TELAIO = :NEW.NUMERO_TELAIO AND DATA_FINE IS NULL;
    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20060, 'Errore: Kart già in riparazione.');
    END IF;
END;

/
-- 9. Controllo peso pilota
CREATE OR REPLACE TRIGGER T_CONTROLLO_PESO_PILOTA
BEFORE INSERT OR UPDATE ON PILOTA FOR EACH ROW
BEGIN
    IF :NEW.PESO < 30 OR :NEW.PESO > 150 THEN
        RAISE_APPLICATION_ERROR(-20070, 'Peso pilota non valido.');
    END IF;
END;
